package Freiburg20240610;

public class ZahlendreieckFor {

    public static void main(String[] args) {
        int zahl;
        String ausgabe = "";

        // For-Schleife
        for (zahl = 0; zahl < 10; zahl++) {
            ausgabe += zahl + " "; // entspricht: ausgabe = ausgabe + zahl + "";
            System.out.println(ausgabe);

        }
    }

}
