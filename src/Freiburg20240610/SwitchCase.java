package Freiburg20240610;

public class SwitchCase {
    public static void main(String[] args) {
        int auswahl = 1;
        switch (auswahl) {
            case 1:
                System.out.println("Auswahl 1");
                break;
            case 2:
                System.out.println("Auswahl 2");
                break;
            case 3:
                System.out.println("Auswahl 3");
                break;
            default:
                System.out.println("kein Auswahl");
                break;
        }
    }
}
